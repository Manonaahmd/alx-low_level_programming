#include "main.h"
/**
 * print_sign - function name
 * Description: print sign of the number
 * @n: the integer value it receives
 * Return: 0
 */
int print_sign(int n)
{
	if (n > 0)
	{
		_putchar('+');
		return (1);
	}
	else if (n < 0)
	{
		_putchar('-');
		return (-1);
	}
	_putchar('0');
	return (0);
}
